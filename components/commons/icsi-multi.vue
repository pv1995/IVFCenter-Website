                <h5 class="treatment-subheading">Preimplantation Diagnosis</h5>
                <div class="treatment-content">
                    Preimplantation genetic diagnosis (PGD) or PGS Preimplantation Genetic Screening is a screening test used to determine if genetic or chromosomal disorders are present in embryos produced through in vitro fertilization (IVF). Preimplantation genetic diagnosis screens embryos before they are transferred to the uterus so couples can make
                    informed decisions about their next steps in the IVF process. Embryos unaffected by the genetic or chromosomal disorder can be selected for transfer to the uterus.
                    <br><br>
                    For couples undergoing IVF, preimplantation genetic diagnosis may be recommended when:
                    <br><br>
                    <ul class="treatment-list">
                        <li>One or both partners has a history of heritable genetic disorders</li>
                        <li>One or both partners is a carrier of a chromosomal abnormality</li>
                        <li>The mother is of advanced maternal age</li>
                        <li>The mother has a history of recurrent miscarriages</li>
                    </ul>
                </div>
                <h5 class="treatment-subheading">For couples with heritable genetic disorders</h5>
                <div class="treatment-content">
                    Preimplantation genetic diagnosis can be used to determine if embryos produced through in vitro fertilization carry a gene mutation associated with a specific genetic disorder, such as cystic fibrosis or muscular dystrophy.
                    <br><br>
                    The benefit of preimplantation genetic diagnosis is that the diagnosis can be made before the embryos are transferred to the uterus and a pregnancy is established. Embryos unaffected by the genetic disorder can be selected for transfer to the uterus, therefore greatly reducing the risk that a couple will pass a genetic disorder onto their child.
                    <br><br>
                    Couples who are at high risk of having a child with a severe genetic disorder may choose preimplantation genetic diagnosis for many reasons, including:
                    <br><br>
                    <ul class="treatment-list">
                        <li>Previous loss of a child from the genetic disorder</li>
                        <li>Previous pregnancy adversely affected by the disorder</li>
                        <li>Objection to terminating a pregnancy affected by the condition</li>
                        <li>Couple has a relative with the disorder</li>
                    </ul>
                </div>
                <h5 class="treatment-subheading">For couples with chromosomal disorders</h5>
                <div class="treatment-content">
                    Preimplantation genetic diagnosis is also offered to couples when one partner has a chromosomal abnormality, such as an unbalanced translocation or aneuploidy. If the abnormality is present in the embryo, the condition could ultimately prevent embryo implantation, leading to pregnancy loss, or result in the birth of a child with congenital malformations (physical problems) or mental retardation.
                    <br><br>
                    The benefit of preimplantation genetic diagnosis is that the diagnosis can be made before the embryos are transferred to the uterus and a pregnancy is established. Embryos unaffected by the chromosomal abnormality can be selected for transfer to the uterus, therefore greatly reducing the risk that the pregnancy will be adversely affected by the chromosomal abnormality.
                    <br><br>
                    Couples who are at high risk of having a child with a chromosomal disorder may choose preimplantation genetic diagnosis for many reasons, including:
                    <br><br>
                    <ul class="treatment-list">
                        <li>Previous pregnancy loss due to a chromosomal abnormality</li>
                        <li>One partner carries a known chromosomal abnormality</li>
                        <li>Multiple pregnancy losses after spontaneous conception.</li>
                    </ul>
                    <br><br>
                    Genetic counseling is an important step to determine if preimplantation genetic diagnosis is an appropriate option for a patient. For couples undergoing IVF who are concerned that their child may inherit a genetic disorder or chromosomal abnormality, genetic counselors are available to discuss options and can advise patients on how raising a handicapped child may affect a family.
                    <br><br>
                    Preimplantation genetic diagnosis is available for couples undergoing IVF. The steps of the IVF process include:
                    <ul class="treatment-list">
                        <li>Medications are used to suppress a woman’s natural menstrual cycle.</li>
                        <li>Her ovaries are then stimulated with medications to produce multiple follicles, each of which may contain an oocyte (egg).</li>
                        <li>The eggs are retrieved from the woman’s ovary by a needle placed in the vagina.</li>
                    </ul>
                    In the lab, the eggs are combined with the male partner’s sperm in a special culture medium that allows fertilization and the growth of high–quality embryos.
                    <br><br>
                    Embryo biopsy may be performed after 3 days of culture in the laboratory. The embryos are typically 8-cell embryos on Day-3 and the process involves the removal of one to two cells.
                    <br><br>
                    After the biopsy and following receipt of the results from the genetic/chromosomal testing, embryo(s) of the best quality that are not affected by the genetic disorder or chromosomal abnormality) are selected for transfer to the uterus. For day 3 embryo biopsies, the embryo is usually transferred “fresh” following two additional days of culture in the laboratory (Day-5 embryo transfer). In some cases, the biopsy will be done on either Day-5 or -6 (trophectoderm biopsy). At this stage, the embryo consists of many cells and is called a blastocyst. Cells are removed from the outer layer of cells called the trophectoderm.
                    <br><br>
                    Following the biopsy of a good quality blastocyst, the blastocyst is then frozen. When the patient receives the results from the genetic testing, the non-affected or chromosomally normal blastocyst(s) are thawed and transferred in a subsequent frozen embryo cycle.
