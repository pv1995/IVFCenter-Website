                <h5 class="treatment-subheading">Testing for heritable genetic disorders</h5>
                <p class="treatment-content">
                    Polymerase Chain Reaction (PCR) is performed on the biopsied cell(s) to determine the presence of a single gene. This is done when a couple has a significantly increased risk of conceiving a child with a severe genetic disorder. When PCR is to be performed, the cell(s) obtained at biopsy is loaded into a tiny tube of medium and sent to for analysis. The specific area of DNA of interest is amplified by making thousands of copies of the DNA through repeated cycles of DNA strand separation and replication. The sample can be analyzed for the presence of a specific sequence of DNA or gene and also for linkage markers near the gene. The biopsied cell(s) are destroyed during this process. Therefore, they cannot be used for another purpose or returned to the embryo.
                </p>
                <h5 class="treatment-subheading">Testing for chromosomal abnormalities</h5>
                <p class="treatment-content">
                    The genetic material (DNA) within the biopsied cell(s) is amplified using a technique called the polymerase chain reaction (PCR). This amplification produces enough DNA to use a second technique, known as array comparative genomic hybridization (aCGH). Array CGH assesses the amount of DNA derived from each chromosome, revealing whether or not there are both a normal amount and correct number of chromosomes. The biopsied cell(s) are destroyed during this process. Therefore, they cannot be used for another purpose or returned to the embryo. aCGH can be used to screen for numeric abnormalities in all chromosomes and/or known rearrangements of chromosomes (translocations). Array CGH does not detect all types of chromosome aberrations or genetic mutations and cannot distinguish between no translocation present and balanced translocation present.
                    <br><br>
                    The results of preimplantation genetic diagnosis are reported to the couple no later than the morning of their scheduled day for embryo transfer. Typically this is five days after oocyte retrieval and in vitro fertilization are performed. Of the embryo(s) that are not affected by the genetic disorder or chromosomal abnormality, the best quality embryo(s) are selected for transfer to the uterus. If additional unaffected and good-quality embryos are available, they may be cryopreserved for a future embryo transfer.
                    <br><br>
                    Preimplantation genetic diagnosis does not replace prenatal testing, such as chorionic villus sampling or amniocentesis. Preimplantation genetic diagnosis provides diagnostic information based on the analysis of a single cell. Therefore, prenatal testing is still recommended and currently remains the standard of care.
                </p>
                <h5 class="treatment-subheading">Blastocyst Transfer</h5>
                <div class="treatment-content">
                    A blastocyst is an embryo that has been developed in the laboratory for five or six days after insemination, in contrast to conventional IVF which involves transferring embryos to the womb two to three days after egg collection and insemination (this is referred to as a Day 2 or 3 transfer).
                    <br><br>
                    <ul class="treatment-list">
                        <li>With a blastocyst transfer, the embryo has advanced to the five or 6-day stage.</li>
                        <li>This means the embryo has divided many more times into many more cells over this period. Blastocysts have a very thin outer shell thus potentially increasing the chances of implantation into the uterine cavity. Most of the blastocyst contains a fluid cavity and it is possible to see the cells which will become the baby and those which will make up the placenta.</li>
                        <li>The cells in a blastocyst have just started to differentiate The surface cells that surround the cavity (just under the outer shell) are called the trophectoderm and will later develop into the placenta A more centrally located group of cells – the inner cell mass, will become the fetus.</li>
                    </ul>
                    <br><br>
                    While the majority of fertilised eggs will develop into a three-day old embryo, only perhaps 40% of these embryos will develop into a blastocyst. Therefore, blastocysts are considered to be a more “select” group of embryos with a higher chance of pregnancy.
                    <br><br>
                    The 5 or 6 day blastocyst is a much more advanced structure than the 3 day old embryo and the real advantage of transferring blastocysts is the high live birth rate associated with blastocyst transfer.
                </div>
                <h5 class="treatment-subheading">Why Blastocyst Transfer</h5>
                <br><br>
                <h5 class="treatment-subheading">Selection of embryos</h5>
                <p class="treatment-content">We know that at least 50% (or higher in women over 40) of embryos are not viable, and many of these arrest their development before the blastocyst stage. A large proportion of these embryos have a chromosome or genetic defect and it is believed that those embryos that failed to develop to the blastocyst would not, in any event, have established a pregnancy. Where there are large numbers of good quality embryos available at the blastocyst stage these can be frozen. DRIFF has excellent post thaw survival and pregnancy rates with frozen blastocysts.</p>
                <h5 class="treatment-subheading">Extended in-vitro culture systems</h5>
                <ul class="treatment-list">
                        <li>In the past, it was difficult to get high quality blastocysts with in vitro culture systems – unless “feeder” cells were utilized – called coculture.</li>
                        <li>THowever, since 1998 more advanced culture media have been commercially available that (if used properly) can yield high blastocyst formation rates. Now blastocyst embryo transfer is a viable IVF treatment option for many couples.</li>
                </ul>
                <h5 class="treatment-subheading">Embryos are transferred to the right place at the right time</h5>
                <p class="treatment-content">Some researchers believe that the conditions in the womb may be more optimal for a blastocyst than a day 2/3 embryo as there are slightly differing conditions in the fallopian tube and the womb on day 2/3.</p>
                <h5 class="treatment-subheading">Higher pregnancy rate in women having Blastocyst Transfer</h5>
                <p class="treatment-content">Data suggests that blastocyst transfer can increase the chances of a live birth but it needs to be remembered that each couple must be considered independently.</p>
                <h5 class="treatment-subheading">Confirmation of development to the blastocyst stage</h5>
                <p class="treatment-content">Our specialists believe that for those patients who have recurrent failure of implantation, extended culture gives an opportunity to examine the embryo quality over a longer period. If the embryos arrest or become fragmented this may help to clarify a potential problem.</p>
                <h5 class="treatment-subheading">Specific situations where blastocyst transfer is applicable</h5>
                <p class="treatment-content">Where single embryo transfer is specifically indicated (eg. previous history of multiple pregnancy, patient preference, uterine anomaly etc) blastocyst transfer may be a particularly useful option.</p>
                <h5 class="treatment-subheading">Extended culture and Blastocyst Transfer with Frozen Embryos</h5>
                <p class="treatment-content">
                    Some patients have large numbers of frozen embryos and it can difficult to know which have the best potential for pregnancy. An option in these cases is to thaw all embryos and culture through to the blastocyst stage to allow the best 1 or 2 embryos to be replaced based on development.
                    <br><br>
                    The main benefit of the blastocyst transfer approach is in the ability to discriminate between different embryos in terms of their quality and implantation potential. It is essential to understand that the extended culture process doesn’t enhance an embryos quality per se; it is principally a method for choosing the ‘front runner’ or ‘runners’ from a group of embryos.
                    <br><br>
                    It is important that we carefully choose the patients who are most likely to benefit. Our general guideline is to proceed to blastocyst transfer only if there are 4 or more good quality embryos available on day 3.
                </p>
