                <h5 class="treatment-subheading">IMSI Technique – Intracytoplasmic Morphologically Selected Sperm Injection</h5>
                <p class="treatment-content">
                    For the creation of a competent embryo two ‘partners’ are needed: a mature oocyte and a 
                    ‘good/healthy’ spermatozoon.
                    <br><br>
                    In the area of assisted reproduction, when we decide to do the classical in vitro fertilization 
                    we leave nature to choose the spermatozoa that will fertilize the available oocytes; on the 
                    contrary when we decide to do ICSI (in cases with male factor infertility) we have to choose 
                    the spermatozoa that we inject into the oocytes. For ICSI, after sperm preparation an optical 
                    magnification of 200x to 400x is used to examine the sample. The best ‘normal looking’ motile 
                    spermatozoa are selected based on their major morphology and then injected into oocytes retrieved 
                    after ovarian stimulation. With ICSI, even men with severe male factor infertility could possibly 
                    achieve pregnancy. However, despite 20 years of technological improvements, both clinical pregnancy 
                    and live birth rates remain relatively low at approximately 35% and 25% per started cycle, 
                    respectively.
                    <br><br>
                    Therefore there is a kind of subjective selection, which is not adequately feasible due to the 
                    microscopic size of the spermatozoa. For the improvement of this selection a new innovative 
                    technique has been developed. The innovative technique is called IMSI (Intracytoplasmic 
                    Morphologically Selected sperm Injection) ‘motile sperm organelle morphology examination’ 
                    (MSOME). This technique requires the analysis of minor morphological criteria using ultra-high 
                    magnification ≥ 6000 times higher magnification when compared to the normal ICSI microscopy and 
                    it implies a powerful microscopy system which allows for the morphologic selection of the ‘good’ 
                    spermatozoon with strict criteria; morphology is associated with genetic quality of the spermatozoon 
                    and consequently with the creation of a good embryo. When using this technique, the motile sperm 
                    fraction is examined based on six subcellular organelles: acrosome, postacrosomal lamina, neck, 
                    mitochondria, tail, and nucleus. Even the minute defects in the spermatozoa become visibl under 
                    this powerful magnification like the size & shape of the head, vacuoles in the spermatozoa head 
                    the midpiece etc.
                    <br><br>
                    Studies comparing the IMSI and ICSI techniques indicate that IMSI does not improve fertilization 
                    rates or the morphologic quality of the embryos, but increases the pregnancy rates and reduces 
                    the miscarriage rates.
                    <br><br>
                    Couples with indication for IMSI are those with Sperm demonstrating high degree of DNA 
                    fragmentation and/or increased abnormal spermatozoa (teratozoospermia), conditions which 
                    often accompany oligoasthenospermia.
                    <br><br>
                    Additionally, IMSI may be beneficial for couples experiencing recurrent IVF failures and/or 
                    recurrent biochemicalpregnancies/miscarriages.
                    <br><br>
                    The promising results of IMSI lead us to adopt the technique in order to strengthen our goal 
                    which is to help our couples to have healthy children.
                </p>
                <h5 class="treatment-subheading">PICSI</h5>
                <p class="treatment-content">
                    In the ICSI procedure, an individual sperm is selected and injected into an oocyte. Until now, 
                    the only technique available to embryologists to select the sperm has been visual observation. 
                    Using PICSI procedure we are able to determine sperm selection in much the same way it happens 
                    in human biology.
                    <br><br>
                    Sperms are placed in PICSI dish containing samples of hyaluronan hydrogel. Hyaluronan is a 
                    naturally occurring biopolymer found in all human cells, including the gel layer surrounding 
                    the oocyte.
                    <br><br>
                    Mature, biochemically competent sperm bind to the hyaluronan where they can be isolated by the 
                    embryologist and used for ICSI. This procedure mimics a key step in the natural fertilization 
                    process, the binding of mature sperm to the oocyte complex. As a result, the selected sperm is 
                    essentially the same as one that would be successful in the natural reproductive process.
                    <br><br>
                    The research proved that hyaluronan-bound PICSI-selected sperm are, in the vast majority of cases, 
                    more mature, exhibit less DNA damage, and have fewer chromosomal aneuploidies.
                    <br><br>
                    We consider the PICSI method to be a biologically more natural and effective form of fertilization 
                    in comparison to ICSI, because only those sperm are chosen for fertilization that are able to form 
                    a bond with the oocyte cumulus complex, i.e. only mature sperm are selected.
                    <br><br>
                    This method is suitable for everyone but we highly recommend it especially in the following cases:
                </p>
